#Haz un programa que pida dos números y muestre si el primero es mayor, menor o igual al segundo.
# (utiliza solo operadores de comparación y lógicos)

num1 = float(input("Ingrese el primer número: "))
num2 = float(input("Ingrese el segundo número: "))
 
(num1 > num2) and print("El primer número es mayor que el segundo número.")
(num1 < num2) and print("El primer número es menor que el segundo número.")
(num1 == num2) and print("Ambos números son los mismos.")



